												<h4>Did you know that the biggest reason for food waste in the UK is ‘not used in time’?</h4>

												<p>41% of our food by weight and 43% by cost is thrown away because it has gone mouldy, mushy or rotten or because it has passed a date label. Freezing food acts like a pause button, which means you can buy yourself a bit more time without the need for a DeLorean.</p>

												<p>If you have fresh food that’s getting close to the Use By date, your freezer – look for it near the fridge – is your BFF. This is because you can freeze right up to the Use By date. Don’t worry if you didn’t ‘freeze on day of purchase’, but always do it by the Use By – otherwise it’s game over.</p>

												<p>Defrosting Tips</p>

												<p>So, you’ve frozen your food. What happens when you want to use it? Here’s Helen’s top advice for defrosting like a boss!</p>

												<p>Food should always be defrosted in the fridge. So, make sure you leave enough time for it to defrost ready for dinner (try getting it out of the freezer the night before, or before you go to work). If this isn’t possible, you can use a microwave on the defrost setting.</p>

												<p>Never defrost food at room temperature. Always check any on-pack guidance and make sure your food is fully defrosted before cooking it. Once defrosted, the pause button is off, and you’ll need to use it pronto: within 24 hours.</p>

												<p>Can I freeze it? Yes, you can!</p>

												<p>Usually – there’s not much that won’t freeze in some way. Here are 5 freezy-peasy tips for some frequently wasted favourites:</p>

												<p>Bread – 20 million slices of bread go to waste every day from UK homes. Freezing bread saves it from the bin – whether the whole loaf goes in as soon as you get home, or you freeze the last few slices for later. You can toast bread straight from frozen and it tastes just as good! When you’re freezing a sliced loaf, give it a tap on a hard surface before freezing to stop the frozen slices from getting stuck together. Blitz leftover crusts and ends into breadcrumbs and freeze.</p>

												<p>Cauliflower and broccoli – When making a big batch of cauliflower or broccoli cheese, split into individual portions for freezing. Cooking to freeze is ideal if you’ve got food that isn’t going to last the distance, or you’ve got too much to get through. This particular dish is super-tasty with a little garlic in the cheese sauce and topped with breadcrumbs (from the freezer).</p>
